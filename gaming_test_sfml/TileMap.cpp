#include "TileMap.h"

bool TileMap::load(const std::string& tileset, sf::Vector2u tileSize, const std::vector<int>& tiles, unsigned int width, unsigned int height, const std::vector<int>& collisionMask)
{
    if (!m_tileset.loadFromFile(tileset)) return false;

    m_tileSize = tileSize;
    m_width = width;
    m_height = height;
    m_collision = collisionMask;

    m_vertices.setPrimitiveType(sf::Triangles);
    m_vertices.resize(width * height * 6);

    for (unsigned y = 0; y < height; ++y) {
        for (unsigned x = 0; x < width; ++x) {
            int tileNumber = tiles[x + y * width];;

            int tu = tileNumber % (m_tileset.getSize().x / tileSize.x);
            int tv = tileNumber / (m_tileset.getSize().x / tileSize.x);

            sf::Vertex* tri = &m_vertices[(x + y * width) * 6];

            // позиции
            tri[0].position = { x * tileSize.x,     y * tileSize.y };
            tri[1].position = { (x + 1) * tileSize.x, y * tileSize.y };
            tri[2].position = { x * tileSize.x,     (y + 1) * tileSize.y };
            tri[3].position = tri[2].position;
            tri[4].position = tri[1].position;
            tri[5].position = { (x + 1) * tileSize.x, (y + 1) * tileSize.y };

            // текст‑координаты
            tri[0].texCoords = { tu * tileSize.x,     tv * tileSize.y };
            tri[1].texCoords = { (tu + 1) * tileSize.x, tv * tileSize.y };
            tri[2].texCoords = { tu * tileSize.x,     (tv + 1) * tileSize.y };
            tri[3].texCoords = tri[2].texCoords;
            tri[4].texCoords = tri[1].texCoords;
            tri[5].texCoords = { (tu + 1) * tileSize.x, (tv + 1) * tileSize.y };
        }
        return true;
    }
    return false;
}

bool TileMap::isWalkable(int tx, int ty) const
{
    if (tx < 0 || ty < 0 || tx >= int(m_width) || ty >= int(m_height))
        return false;
    return m_collision[tx + ty*m_width] == 0;
}

void TileMap::draw(sf::RenderTarget& target, sf::RenderStates states) const
{
    states.transform *= getTransform();
    states.texture = &m_tileset;
    target.draw(m_vertices, states);
}



